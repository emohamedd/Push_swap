#include "push_swap_bonus.h"

void	check_param(char **av)
{
	int	i;
	int	j;

	i = 0;
	while (av[i])
	{
		j = 0;
		while (av[i][j])
		{
			if (((av[i][j] == '-' && av[i][1] == '\0') || (av[i][j] == '+'
						&& av[i][1] == '\0')) || av[i][j] == ' ')
				err();
			j++;
		}
		i++;
	}
}
