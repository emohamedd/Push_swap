#include "push_swap.h"

void check_param(char **av)
{
    int i = 0;
    int j;

    while (av[i])
    {
        j = 0;
        while(av[i][j])
        {
            if ((av[i][j] == '-' || av[i][j] == '+') || av[i][j] == ' ')
                err();
            j++;
        }
        i++;
    }
}